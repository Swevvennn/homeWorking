#include <unistd.h>

int	ft_str_is_printable(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (32 <= str[i])
		{
			i++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
