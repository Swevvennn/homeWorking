int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	int				s;

	i = 0;
	s = 0;
	while (i < n && s == 0 && (s1[i] || s2[i]))
	{
		s = s1[i] - s2[i];
		i++;
	}
	return (s);
}
