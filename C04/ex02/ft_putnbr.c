#include <unistd.h>
#include <limits.h>

void	ft_putnbr(int n)
{
	if (n == INT_MIN)
	{
		write(1, "-2147483648", 11);
	}
	else
	{
		if (n < 0)
		{
			write(1, "-", 1);
			n = -n;
		}
		if (n > 10)
		{
			ft_putnbr(n / 10);
		}
		write(1, n % 10 + 48, 1);
	}
}
