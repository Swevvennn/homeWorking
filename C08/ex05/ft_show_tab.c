#include "ft_stock_str.h"
#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	to_write;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else
	{
		if (nb < 0)
		{
			write(1, "-", 1);
			nb = -nb;
		}
		if (nb >= 10)
		{
			ft_putnbr(nb / 10);
		}
		to_write = nb % 10 + 48;
		write(1, &to_write, 1);
	}
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

void	ft_show_tab(struct s_stock_str *par)
{
	ft_putnbr(par->size);
	write(1, "\n", 1);
	ft_putstr(par->str);
	write(1, "\n", 1);
	ft_putstr(par->copy);
	write(1, "\n", 1);
}
