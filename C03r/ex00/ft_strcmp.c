int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	s;

	i = 0;
	s = 0;
	while (s == 0 && (s1[i] || s2[i]))
	{
		s = s1[i] - s2[i];
		i++;
	}
	return (s);
}
